void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int tab_cpy[size];

	i = 0;
	while (i != size)
	{
		tab_cpy[i] = tab[i];
		i++;
	}
	i = 0;
	while (size != 0)
	{
		tab[i] = tab_cpy[size - 1];
		size--;
	}
}
