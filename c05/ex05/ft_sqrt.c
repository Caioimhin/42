int		ft_sqrt(int nb)
{
	int i;
	int res;

	i = 0;
	res = 0;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (nb);
	while (i < nb && i <= 46340)
	{
		res = i * i;
		if (res == nb)
			return (i);
		i++;
	}
	return (0);
}
